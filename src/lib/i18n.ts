export type MessageKey = keyof typeof messages.en;

export function getLocale(): Locale {
    const configured = SITE_CONFIG.i18n?.defaultLocale;
    if (configured && (SUPPORTED_LOCALES as readonly string[]).includes(configured)) {
        return configured as Locale;
    }
    return 'en';
}

export function t(key: MessageKey, params?: Record<string, string | number>, locale?: Locale): string {
    const currentLocale = locale || getLocale();
    const dictionary = messages[currentLocale] || messages.en;
    let text: string = dictionary[key] || messages.en[key] || key;
    if (params) {
        for (const [paramKey, value] of Object.entries(params)) {
            text = text.replace(new RegExp(`\\{${paramKey}\\}`, 'g'), String(value));
        }
    }
    return text;
}
